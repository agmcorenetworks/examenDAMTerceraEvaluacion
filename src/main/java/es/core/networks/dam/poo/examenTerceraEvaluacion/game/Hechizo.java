package es.core.networks.dam.poo.examenTerceraEvaluacion.game;

public class Hechizo {

	int daño;	
	String name;
	
	public Hechizo() {
		this.name="sectum sembra";
		this.daño=2;
	}
	
	
	
	public int getDaño() {
		return daño;
	}
	public void setDaño(int daño) {
		this.daño = daño;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
